var x = 5; // Declares a variable 'x' with value 5 using var
let y = 10; // Declares a variable 'y' with value 10 using let
const z = 15; // Declares a variable 'z' with value 15 using const

x = 7; // Reassigns the value of x
y = 12; // Reassigns the value of y
// z = 17; // This would cause an error because const variables cannot be reassigned

console.log(x); // Output: 7
console.log(y); // Output: 12
console.log(z); // Output: 152