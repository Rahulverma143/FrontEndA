// Subtraction of Two number 

let a = 20;
let b = 10;
let res = a - b;
console.log(a); // output 20
console.log(b); // output 10
console.log("a =", a); // output a = 20
console.log("b =", b); // output a = 10
console.log(res); // output 10;
console.log(typeof res); // output number
console.log("res", res); // output res 10
console.log("res", a - b); // output res 10
console.log("a =", a, "& b = ", b ); // output a = 20 & b =  10
console.log("a =", a, "& b = ", b, "& res =", a - b ); // output a = 20 & b =  10 & res = 10

