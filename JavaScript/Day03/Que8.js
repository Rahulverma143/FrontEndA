// Assignment opertions

let z = 10;
console.log("z =", z);   // output z = 10

let n = 10;   // = assigns a value to a variable.
n += 5;   // += adds and assigns the result to the variable.
console.log(n);  // output 15

let a = 10;
a *= 2;  // *= multiplies and assigns the result to the variable.
console.log(a); // output 20
a = a * 5;
console.log(a);  // output 100

let b = 20;
b *= 10;
b = b * 10;
console.log(b); // output 2000

let c = 100;
c /= 10;
console.log(c);  // output  10 
c = c / 10;
console.log(c); // output 1

let x = 50;
x %= 2;
console.log(x);  // output 0
x = x % 2;
console.log(x);  // output 0


let y = 2;
y **=2;
console.log(y);  // output 4
y = y ** 2;
console.log(y);  // output 16

let text = "Hello";
text += " World";
