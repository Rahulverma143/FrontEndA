// Increament ++

let a = 10;
console.log(a); // output 10
console.log("a =", a); // output 10
a = a + 1;
console.log(a); // output 11
a++; // post increament  (phele add then show )
console.log(a); // output 12
++a;  // pre increament ( phele add then show)
console.log(a); // output 13
console.log("++a = ", ++a); // output 14
console.log("a++ =", a); // output 14
console.log("a =", a); // output a = 14

