// Addition of Two number 

let a = 10;
let b = 20;
let res = a + b;
console.log(a); // output 10
console.log(b); // output 20
console.log("a =", a); // output a = 10
console.log("b =", b); // output a = 20
console.log(res); // output 30;
console.log(typeof res); // output number
console.log("res", res); // output res 30
console.log("res", a + b); // output res 30
console.log("a =", a, "& b = ", b ); // output a = 10 & b =  20
console.log("a =", a, "& b = ", b, "& res =", a + b ); // output a = 10 & b =  20 & res = 30

